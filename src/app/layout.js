// login function
function Login() {
  const router = useRouter();
  const [formData, setFormData] = useState({
    name: "",
    password: "",
  });
  const [userData, setUserData] = useState({
    newPassword: "",
    confirmPassword: "",
  });
  const [user, setUser] = useState(null);

  // console.log(user);
  const [loginInProgress, setLoginInProgress] = useState(false);
  const [isPasswordMismatch, setIsPasswordMismatch] = useState(false);
  const [loginPassword, setLoginPassword] = useState("");

  const handleInputChange = (e) => {
    const { name, value } = e.target;
    setFormData((prevData) => ({
      ...prevData,
      [name]: value,
    }));
  };

  const handleInputChangeUser = (e) => {
    const { name, value } = e.target;
    setUserData((prevData) => ({
      ...prevData,
      [name]: value,
    }));
  };

  const [loginUserData] = useLoginUserDataMutation();
  const handleSubmit = useCallback(
    async (e) => {
      // e.preventDefault();

      try {
        setLoginInProgress(true);
        const result = await loginUserData(formData);
        // console.log(result?.data?.result?.userData?.isFirstSignIn);

        if (result?.data?.status === 200) {
          localStorage.setItem(
            "user",
            JSON.stringify(result.data.result.userData)
          );
          // console.log(result?.data?.isFirstSignIn);
          if (result?.data?.result?.userData?.isFirstSignIn === true) {
            toast.success("Reset Your Password!");
          }
          if (result?.data?.result?.userData?.isFirstSignIn === false) {
            toast.success("Login Success!");
          }
          const userResult = localStorage.getItem("user");
          setUser(JSON.parse(userResult));
          setLoginPassword(formData.password);
        } else {
          toast.error("Login failed. Please check your credentials.");
        }
      } catch (error) {
        console.error("Form submission failed", error);
        toast.error("Form submission failed. Please try again.");
      } finally {
        setLoginInProgress(false);
      }
    },
    [formData, setLoginPassword, setLoginInProgress]
  );

  useEffect(() => {
    // console.log(user);
    if (user?.isFirstSignIn === false) {
      window.location.href = "leads";
    }
  }, [user, userData]);

  // console.log(user);
  const [resetPassword] = useResetPasswordMutation();
  const handleSubmitPassword = useCallback(async () => {
    if (userData.newPassword !== userData.confirmPassword) {
      setIsPasswordMismatch(true);
      toast.error("Passwords do not match!");
      return;
    }
    const result = {
      newPassword: userData.confirmPassword,
      password: loginPassword,
      id: user?.id,
    };
    const finalResult = await resetPassword(result);
    if (finalResult?.data?.status === 200) {
      localStorage.setItem("user", JSON.stringify(finalResult?.data?.result));
      toast.success("Login Success!");
      router.push("/leads");
      setTimeout(() => {
        window.location.reload();
      }, 1800);
    }
    // console.log(finalResult);
  }, [userData]);

  const [showPassword, setShowPassword] = useState(false);
  const toggleShowPassword = () => {
    setShowPassword(!showPassword);
  };

  const handleBackLogin = () => {
    localStorage.removeItem("user");
    window.location.reload();
  };

  const handleKeyPress = (event) => {
    if (event.key === "Enter") {
      handleSubmit();
    }
  };

  useEffect(() => {
    window.addEventListener("keypress", handleKeyPress);

    return () => {
      window.removeEventListener("keypress", handleKeyPress);
    };
  }, [formData]);

  return (
    <>
      <ToastContainer />
      {user?.isFirstSignIn === false || user === null ? (
        <Box
          sx={{
            display: "flex",
            justifyContent: "space-around",
            alignItems: "center",
            width: "100%",
            height: "100%",
            flexDirection: "column",
          }}
        >
          <Typography>SIGN IN TO CONTINUE</Typography>
          <Box
            width={{
              width: "100%",
              display: "flex",
              justifyContent: "center",
            }}
          >
            <TextField
              sx={{
                width: "90%",
                "& .MuiOutlinedInput-root .MuiOutlinedInput-notchedOutline": {
                  borderRadius: "19px",
                },
              }}
              name="name"
              label="Username / Email / Phone"
              variant="outlined"
              onChange={handleInputChange}
            />
          </Box>
          <Box
            width={{
              width: "100%",
              display: "flex",
              justifyContent: "center",
            }}
          >
            <TextField
              sx={{
                width: "90%",
                // padding: "5px",
                "& .MuiOutlinedInput-root .MuiOutlinedInput-notchedOutline": {
                  borderRadius: "19px",
                },
              }}
              name="password"
              label="Password"
              type={showPassword ? "text" : "password"}
              variant="outlined"
              onChange={handleInputChange}
              InputProps={{
                endAdornment: (
                  <InputAdornment position="end">
                    <IconButton onClick={toggleShowPassword} edge="end">
                      {showPassword ? <Visibility /> : <VisibilityOff />}
                    </IconButton>
                  </InputAdornment>
                ),
              }}
            />
          </Box>
          <Button
            sx={{
              // border: "1px solid black",
              width: "90%",
              backgroundColor: "black",
              color: "white",
              height: "45px",
              borderRadius: "20px",
              "&:hover": {
                backgroundColor: "black",
                boxShadow: "none",
                border: "none",
              },
            }}
            onClick={handleSubmit}
            disabled={loginInProgress}
          >
            {loginInProgress ? (
              <Typography sx={{ color: "gray" }}>Logging in...</Typography>
            ) : (
              <Typography sx={{ color: "white" }}>Login</Typography>
            )}
          </Button>
        </Box>
      ) : (
        <Box
          sx={{
            display: "flex",
            justifyContent: "space-around",
            alignItems: "center",
            width: "100%",
            height: "100%",
            flexDirection: "column",
          }}
        >
          <Typography>Reset password</Typography>
          <Box
            width={{
              width: "100%",
              display: "flex",
              justifyContent: "center",
            }}
          >
            <TextField
              sx={{
                width: "90%",
                "& .MuiOutlinedInput-root .MuiOutlinedInput-notchedOutline": {
                  borderRadius: "19px",
                },
              }}
              name="newPassword"
              type="password"
              label="Password"
              value={userData.newPassword}
              variant="outlined"
              onChange={handleInputChangeUser}
            />
          </Box>
          <Box
            width={{
              width: "100%",
              display: "flex",
              justifyContent: "center",
            }}
          >
            <TextField
              sx={{
                width: "90%",
                // padding: "5px",
                "& .MuiOutlinedInput-root .MuiOutlinedInput-notchedOutline": {
                  borderRadius: "19px",
                },
              }}
              name="confirmPassword"
              label="Confirm Password"
              type={showPassword ? "text" : "password"}
              variant="outlined"
              value={userData.confirmPassword}
              onChange={handleInputChangeUser}
              InputProps={{
                endAdornment: (
                  <InputAdornment position="end">
                    <IconButton onClick={toggleShowPassword} edge="end">
                      {showPassword ? <Visibility /> : <VisibilityOff />}
                    </IconButton>
                  </InputAdornment>
                ),
              }}
            />
          </Box>
          <Typography
            sx={{
              // border: "1px solid black",
              width: "90%",
              textAlign: "end",
              fontSize: "14px",
            }}
          >
            <Button
              onClick={handleBackLogin}
              sx={{ color: "black", fontSize: "12px" }}
            >
              Back to Login ?
            </Button>
          </Typography>
          <Button
            sx={{
              // border: "1px solid black",
              width: "90%",
              backgroundColor: "black",
              color: "white",
              height: "45px",
              borderRadius: "20px",
              "&:hover": {
                backgroundColor: "black",
                boxShadow: "none",
                border: "none",
              },
            }}
            onClick={handleSubmitPassword}
            disabled={loginInProgress}
          >
            <Typography sx={{ color: "white" }}>Confirm</Typography>
          </Button>
        </Box>
      )}
    </>
  );
}

